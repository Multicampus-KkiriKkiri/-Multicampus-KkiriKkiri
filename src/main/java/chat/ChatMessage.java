package chat;

public class ChatMessage {
    String chatRoomId;
    String message;
    String type;
    String userId;
    
	public String getChatRoomId() {
		return chatRoomId;
	}
	
	public String getMessage() {
		return message;
	}
	
	public String getType() {
		return type;
	}
	
	public String getUserId() {
        return userId;
    }
     
}